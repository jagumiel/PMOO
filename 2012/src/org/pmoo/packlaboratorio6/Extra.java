package org.pmoo.packlaboratorio6;

public class Extra extends ConRetencion
{
	protected int nHoras;
	protected int preciosHora;
	protected int justificacion;
	
	public int getnHoras() {
		return nHoras;
	}
	public void setnHoras(int nHoras) {
		this.nHoras = nHoras;
	}
	public int getPreciosHora() {
		return preciosHora;
	}
	public void setPreciosHora(int preciosHora) {
		this.preciosHora = preciosHora;
	}
	public int getJustificacion() {
		return justificacion;
	}
	public void setJustificacion(int justificacion) {
		this.justificacion = justificacion;
	}
}
